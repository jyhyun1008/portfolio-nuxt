import {onMounted} from 'vue';

onMounted(()=> {

    document.querySelector('.overflow-width').style.left=`0`;
    document.querySelector('#content-scroll').addEventListener('wheel',function(e){
        if(e.deltaY > 0){
            if (parseInt(document.querySelector('.overflow-width').style.left) > -1800) {
                e.preventDefault();
                e.stopPropagation();
                document.querySelector('.overflow-width').style.top=`0%`;
                document.querySelector('.overflow-width').style.left = `${parseInt(document.querySelector('.overflow-width').style.left) - 15}px`;
            }
        } if (e.deltaY < 0) {
            if (parseInt(document.querySelector('.overflow-width').style.left) < 0) {
                e.preventDefault();
                e.stopPropagation();
                document.querySelector('.overflow-width').style.top=`0%`;
                document.querySelector('.overflow-width').style.left = `${parseInt(document.querySelector('.overflow-width').style.left) + 15}px`;
            }
        }
    });
    document.querySelector('#content-scroll').addEventListener('touchstart', touchstart);
    var startY;

    function touchstart(e) {
        startY = e.touches[0].clientY;
    }

    document.querySelector('#content-scroll').addEventListener('touchmove',function(e){

        var deltaY = startY - e.touches[0].clientY ;
        console.log(deltaY)
        if(deltaY > 0){
            if (parseInt(document.querySelector('.overflow-width').style.left) > -1800) {
                e.preventDefault();
                e.stopPropagation();
                document.querySelector('.overflow-width').style.top=`0%`;
                document.querySelector('.overflow-width').style.left = `${parseInt(document.querySelector('.overflow-width').style.left) - 30}px`;
            }
        } if (deltaY < 0) {
            if (parseInt(document.querySelector('.overflow-width').style.left) < 0) {
                e.preventDefault();
                e.stopPropagation();
                document.querySelector('.overflow-width').style.top=`0%`;
                document.querySelector('.overflow-width').style.left = `${parseInt(document.querySelector('.overflow-width').style.left) + 30}px`;
            }
        }
    });
})
